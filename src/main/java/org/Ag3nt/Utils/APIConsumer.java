package org.Ag3nt.Utils;

public class APIConsumer {

    // 消费基本数据类型的方法

    /**
     * 消费 byte 类型数据
     * @param b 要消费的 byte 值
     */
    static public void consumeBasicByte(byte b) {}

    /**
     * 消费 short 类型数据
     * @param s 要消费的 short 值
     */
    static public void consumeBasicShort(short s) {}

    /**
     * 消费 int 类型数据
     * @param i 要消费的 int 值
     */
    static public void consumeBasicInt(int i) {}

    /**
     * 消费 long 类型数据
     * @param l 要消费的 long 值
     */
    static public void consumeBasicLong(long l) {}

    /**
     * 消费 float 类型数据
     * @param f 要消费的 float 值
     */
    static public void consumeBasicFloat(float f) {}

    /**
     * 消费 double 类型数据
     * @param d 要消费的 double 值
     */
    static public void consumeBasicDouble(double d) {}

    /**
     * 消费 char 类型数据
     * @param c 要消费的 char 值
     */
    static public void consumeBasicChar(char c) {}

    /**
     * 消费 boolean 类型数据
     * @param b 要消费的 boolean 值
     */
    static public void consumeBasicBoolean(boolean b) {}

    // 消费引用数据类型的方法

    /**
     * 消费 Object 类型数据
     * @param o 要消费的 Object 对象
     */
    static public void consumeRefObject(Object o) {}

    /**
     * 消费 String 类型数据
     * @param str 要消费的 String 对象
     */
    static public void consumeRefString(String str) {}

    /**
     * 消费数组类型数据（以 int 数组为例）
     * @param arr 要消费的 int 数组
     */
    static public void consumeRefIntArray(int[] arr) {}

    /**
     * 消费数组类型数据（以 Object 数组为例）
     * @param arr 要消费的 Object 数组
     */
    static public void consumeRefObjectArray(Object[] arr) {}
}
